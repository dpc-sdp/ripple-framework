<script lang="ts">
export default { name: 'RplVerticalNav' }
</script>

<script setup lang="ts">
import { RplVerticalNavItem } from './constants'
import RplVerticalNavLink from './link.vue'
import RplVerticalNavToggle from './toggle.vue'
import RplVerticalNavChildList from './child-list.vue'
import RplExpandable from '../expandable/expandable.vue'
import { useExpandableState } from '../../composables/useExpandableState'

interface Props {
  title: string
  items: RplVerticalNavItem[]
}

const props = defineProps<Props>()

const initialActiveIndexes: string[] = props.items.reduce(
  (result: string[], current: RplVerticalNavItem): string[] => {
    if (current.active) {
      return [...result, current.id]
    }

    return result
  },
  []
)

const { isItemExpanded, toggleItem } = useExpandableState(
  initialActiveIndexes,
  props.items.length
)
</script>

<template>
  <nav class="rpl-vertical-nav">
    <h3 class="rpl-vertical-nav__heading rpl-type-h3-fixed">{{ title }}</h3>

    <ul
      class="
        rpl-vertical-nav__list rpl-vertical-nav__list--level-1
        rpl-type-p-small
      "
    >
      <li
        v-for="(item, index) in items"
        :key="index"
        :class="{
          'rpl-vertical-nav__list-item': true,
          'rpl-vertical-nav__list-item--expanded': isItemExpanded(item.id)
        }"
      >
        <RplVerticalNavToggle
          v-if="item.items"
          :id="`rpl-vertical-nav-${item.id}-toggle`"
          :text="item.text"
          @click="toggleItem(item.id)"
        />

        <RplExpandable
          v-if="item.items"
          :aria-labelledby="`rpl-vertical-nav-${index}-toggle`"
          :aria-hidden="isItemExpanded(item.id) === false ? 'true' : null"
          :expanded="isItemExpanded(item.id)"
          class="rpl-vertical-nav__list-item-children"
        >
          <RplVerticalNavChildList
            :items="item.items"
            :level="2"
            :is-expanded="isItemExpanded(item.id)"
          />
        </RplExpandable>

        <RplVerticalNavLink
          v-else
          :text="item.text"
          :href="item.url"
          :active="item?.active"
        />
      </li>
    </ul>
  </nav>
</template>

